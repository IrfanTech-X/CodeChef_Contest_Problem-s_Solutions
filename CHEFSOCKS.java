import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sn=new Scanner(System.in);
		int A=sn.nextInt();
		int X=sn.nextInt();
		int Y=sn.nextInt();
		int r=X+Y;
		if(r>=A){
		    System.out.println("Yes");
		}else{
		    System.out.println("No");
		}

	}
}
