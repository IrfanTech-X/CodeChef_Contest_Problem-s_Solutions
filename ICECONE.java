import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
    public static void main(String[] args) throws java.lang.Exception
    {
        Scanner sn = new Scanner(System.in);
        int X = sn.nextInt();
        int Y = sn.nextInt();
       
        int result = Math.min(X, Y);
        System.out.println(result);
}
}