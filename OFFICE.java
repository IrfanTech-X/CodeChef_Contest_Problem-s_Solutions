import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sn=new Scanner(System.in);
		int t=sn.nextInt();
		while(t!=0){
		    
		    int x=sn.nextInt();
		    int y=sn.nextInt();
		    
		    int result= (x*4)+y;
		    System.out.println(result);
		    
		    t--;
		}

	}
}
